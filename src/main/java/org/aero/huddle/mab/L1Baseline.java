package org.aero.huddle.mab;

public class L1Baseline {
	private String taskType;
	private String frameworkTag;
	private String framework;
	private int l1KeyId;
	private int l1Number;
	private String l1TaskNumber;
	private String l1Task;
	
	public String getTaskType() {
		return taskType;
	}
	public void setTaskType(String taskType) {
		this.taskType = taskType;
	}
	public String getFrameworkTag() {
		return frameworkTag;
	}
	public void setFrameworkTag(String frameworkTag) {
		this.frameworkTag = frameworkTag;
	}
	public String getFramework() {
		return framework;
	}
	public void setFramework(String framework) {
		this.framework = framework;
	}
	public int getL1KeyId() {
		return l1KeyId;
	}
	public void setL1KeyId(int l1KeyId) {
		this.l1KeyId = l1KeyId;
	}
	public int getL1Number() {
		return l1Number;
	}
	public void setL1Number(int l1Number) {
		this.l1Number = l1Number;
	}
	public String getL1TaskNumber() {
		return l1TaskNumber;
	}
	public void setL1TaskNumber(String l1TaskNumber) {
		this.l1TaskNumber = l1TaskNumber;
	}
	public String getL1Task() {
		return l1Task;
	}
	public void setL1Task(String l1Task) {
		this.l1Task = l1Task;
	}
}
